public class Actividades {
	public static Double Act1, Act2, Act3, Act4;
	public Actividades(Double T1, Double T2, Double T3, Double T4) {
		setAct1(T1);
		setAct2(T2);
		setAct3(T3);
		setAct4(T4);
	}
	
	public void setAct1(Double T1){
		Act1 = T1;
	}
	public double getAct1() {
		return Act1;
	}
	public void setAct2(Double T2) {
		Act2 = T2;
	}
	public double getAct2() {
		return Act2;
	}
	public void setAct3(Double T3) {
		Act3 = T3;
	}
	public double getAct3() {
		return Act3;
	}
	public void setAct4(Double T4) {
		Act4 = T4;
	}
	public double getAct4() {
		return Act4;
	}
}
